[CmdletBinding()]
param(
    [Parameter(Mandatory = $true)]
    [String]$WorkItemProjDir, # Path to proj file containing test workitems generated by GenerateHelixWorkItems.ps1

    [Parameter(Mandatory = $true)]
    [String]$WorkItemProjFileNameFilter, # File name filter for work item proj files

    [Parameter(Mandatory = $true)]
    [String]$TestPayloadDir, # Path to the test binaries

    [Parameter(Mandatory = $true)]
    [String]$UploadRoot, # Logs, screenshots, etc. from the tests will be copied here so they can be uploaded.

    [Parameter(Mandatory = $true)]
    [int]$JobPositionInPhase, # JobPositionInPhase and TotalJobsInPhase determine the selection of work items to run

    [Parameter(Mandatory = $true)]
    [int]$TotalJobsInPhase,

    [int]$RerunPassesRequiredToAvoidFailure = 5,

    [switch]$SkipCopyTestResultsXml
)

# This script is given a proj file generated by GenerateHelixWorkItems.ps1 containing a set of work items to execute.
# Usually these work items are executed by Helix, but in this case we are going to run them directly on the Pipeline agent
# This script is run in parallel across multiple build agents. We choose which items to run based on JobPositionInPhase/TotalJobsInPhase

$ErrorActionPreference = 'Stop'

Write-Host "WorkItemProjFile = $WorkItemProjFile"
Write-Host "WorkItemProjDir = $WorkItemProjDir"
Write-Host "TestPayloadDir = $TestPayloadDir"
Write-Host "UploadRoot = $UploadRoot"
Write-Host "JobPositionInPhase = $JobPositionInPhase"
Write-Host "TotalJobsInPhase = $TotalJobsInPhase"

if(!(Test-Path $UploadRoot))
{
    mkdir $UploadRoot
}

$dumpsDir = "C:\dumps"
if(!(Test-Path $dumpsDir))
{
    mkdir $dumpsDir
}


$projFiles = Get-ChildItem -Path $WorkItemProjDir -Filter $WorkItemProjFileNameFilter

$workItems =@()
foreach($projFile in $projFiles)
{
    [xml]$content = Get-Content $projFile.FullName 
    foreach($item in @($content.Project.ItemGroup.HelixWorkItem))
    {
        if($item)
        {
            $workItems += $item
        }
    }
}

# We choose a 'slice' of work items to run based on JobPositionInPhase/TotalJobsInPhase
$workItemsToRun = @()
$i = $JobPositionInPhase - 1; # JobPositionInPhase starts at 1.
while($i -lt $workItems.Count)
{
    $workItemsToRun += $workItems[$i]
    $i += $TotalJobsInPhase
}

Write-Host "Work items to run:"
$workItemsToRun.Include
Write-Host ""

if ($workItemsToRun.Count -eq 0)
{
    Write-Host "No work items matched for this agent. Skipping test execution."
    exit 0
}

foreach($workItem in $workItemsToRun)
{
    $command = $workItem.Command
    $precommand = $workItem.PreCommands
    $workItemName = $workItem.Include
    $resultsFileName = ".\testResults-$workItemName.xml"
    $workItemUploadRoot = Join-Path $uploadRoot $workItemName
    mkdir $workItemUploadRoot

    $consoleLogOutputFile = Join-Path $workItemUploadRoot "console.log"

    $env:HELIX_CORRELATION_PAYLOAD = $TestPayloadDir
    $env:HELIX_WORKITEM_UPLOAD_ROOT = $workItemUploadRoot
    $env:rerunPassesRequiredToAvoidFailure = $RerunPassesRequiredToAvoidFailure
    $env:HELIX_DUMP_FOLDER = $dumpsDir

    Write-Host "Running $workItemName work item"

    # Due to the presence of quotes, symbols, etc. we cannot directly invoke $command from Powershell. So instead we write it to
    # a .cmd file and invoke that.
    $fullCommand = "$precommand`n$command"
    Write-Host $fullCommand
    Out-File -FilePath "temp-runworkitem.cmd" -Encoding ascii -InputObject $fullCommand
    & ./temp-runworkitem.cmd | Tee-Object -file $consoleLogOutputFile

    if(!$SkipCopyTestResultsXml)
    {
        if (!(Test-Path ".\testResults.xml"))
        {
            # We expect the work item to produce a 'testResults.xml' file. If it didn't, something bad happened.
            Throw "Expected testResults.xml to be found. Something unexpected happened."
        }
    
        Move-Item .\testResults.xml $resultsFileName
    }

    Get-ChildItem -Path $workItemUploadRoot -Filter *_subresults.json | Move-Item -Destination $uploadRoot
}

# Upload at most 3 dumps from this run
$files = Get-ChildItem -Path $dumpsDir -Filter *.dmp | Select-Object -First 3
foreach($file in $files)
{
    Move-Item $file.FullName $uploadRoot -Force
}
